#include "main.h"
/**
* positive_or_negative - A debugging function to test for neg or pos num.
*
* @i: argument passed to the function
*
* Return: 0 (Success)
*/
void positive_or_negative(int i)
{
	if
	(i > 0)
	{
		printf("%d is positive\n", i);
	}
	if
	(i == 0)
	{
		printf("%d is zero\n", i);
	}
	if
	(i < 0)
	{
		printf("%d is negative\n", i);
	}
}
