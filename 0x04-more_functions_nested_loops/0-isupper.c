#include "main.h"
/**
* _isupper - A function to check for uppercase character
*
* @c: a character to search for which is passed as argument
*
* Return: returns 1 if success or 0 otherwise
*/
int _isupper(int c)
{
	if
	(c <= 65 && c <= 90)
	{
		return (1);
	}
	else
	{
		return (0);
	}
	_putchar('\n');
}
